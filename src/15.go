package main

import (
    "fmt"
)

func main() {
    // Add two numbers
    sum := 5 + 3
    fmt.Println("Sum:", sum)

    // Subtract two numbers
    difference := 7 - 2
    fmt.Println("Difference:", difference)

    // Multiply two numbers
    product := 4 * 6
    fmt.Println("Product:", product)

    // Divide two numbers
    quotient := 10 / 3
    fmt.Println("Quotient:", quotient)
}
